public class Student {
    int id;
    String fullName;
    String email;
    int age;
    double grade;
    public Student(int id, String fullName, String email, int age, double grade) {
        this.id = id;
        this.fullName = fullName;
        this.email = email;
        this.age = age;
        this.grade = grade;
    }
}
